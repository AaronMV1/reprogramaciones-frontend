CREATE TABLE reprogramaciones.usuario (
	id SERIAL PRIMARY KEY,
	nombres VARCHAR(100),
    apellidos VARCHAR(100),
    correo VARCHAR(100),
    perfil VARCHAR(50),
    programa VARCHAR(100),
	activo BOOLEAN,
	usuario_responsable VARCHAR(50));

CREATE TABLE reprogramaciones.configuracion (
    id SERIAL PRIMARY KEY,
    limite INTEGER,
    plazo INTEGER,
    anticipacion INTEGER,
    cdocente BOOLEAN,
    calumno BOOLEAN,
    cdoa BOOLEAN,
    ccoordinador_programa BOOLEAN,
    ccoordinador_ambiente BOOLEAN);

CREATE TABLE reprogramaciones.semestre (
    id SERIAL PRIMARY KEY,
    grado VARCHAR(100),
    codigo VARCHAR(100),
    fecha_inicio DATE DEFAULT CURRENT_DATE,       -- Solo fecha
    fecha_cierre TIMESTAMP DEFAULT NOW(),         -- Fecha + hora exacta
    usuario_responsable VARCHAR(50)
);



