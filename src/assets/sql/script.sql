--  ORACLE



CREATE TABLE IDO_REPROGRAMACION_CLASES.USUARIO (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    nombres VARCHAR2(100),
    apellidos VARCHAR2(100),
    correo VARCHAR2(100),
    perfil VARCHAR2(50),
    programa VARCHAR2(100),
    activo CHAR(1) CHECK (activo IN ('Y','N')),
    usuario_responsable VARCHAR2(50));
CREATE TABLE IDO_REPROGRAMACION_CLASES.CONFIGURACION (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    limite INTEGER,
    plazo INTEGER,
    plazo INTEGER,
    cdocente CHAR(1) CHECK (cdocente IN ('Y','N')),
    calumno CHAR(1) CHECK (calumno IN ('Y','N')),
    cdoa CHAR(1) CHECK (cdoa IN ('Y','N')),
    ccoordinador_programa CHAR(1) CHECK (ccoordinador_programa IN ('Y','N')),
    ccoordinador_ambiente CHAR(1) CHECK (ccoordinador_ambiente IN ('Y','N')));

CREATE TABLE IDO_REPROGRAMACION_CLASES.SEMESTRE (
    id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    grado VARCHAR2(50),
    cod_grado VARCHAR2(10),
    semestre VARCHAR(50),
    cod_semestre VARCHAR2(10),
    fecha_inicio DATE,
    fecha_cierre DATE,
    fecha_creacion DATE,
    usuario_creacion VARCHAR2(100),
    fecha_modificacion DATE,
    usuario_modificacion VARCHAR2(100)
);



